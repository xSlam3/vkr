def _get_admin_user(db):
    users = UserRepository.list_all(db)
    for user in users:
        if user.role == UserRole.admin:
            return user
    raise RuntimeError("Администратор не найден. Сначала создайте admin-пользователя.")


def main() -> None:
    db = SessionLocal()
    try:
        admin = _get_admin_user(db)
        created_categories = 0
        created_articles = 0

        for category_name, articles in SEED_DATA.items():
            category = KnowledgeRepository.get_category_by_name(db, category_name)
            if category is None:
                category = KnowledgeService.create_category(db, CategoryCreate(name=category_name))
                created_categories += 1

            existing_titles = {article.title for article in KnowledgeRepository.list_articles(db, category_id=category.id)}
            for article_data in articles:
                if article_data["title"] in existing_titles:
                    continue
                KnowledgeService.create_article(
                    db,
                    KnowledgeArticleCreate(
                        title=article_data["title"],
                        text_content=article_data["text"],
                        category_id=category.id,
                        media_url=None,
                        media_type=None,
                    ),
                    admin,
                )
                created_articles += 1

        total_categories = len(KnowledgeRepository.list_categories(db))
        total_articles = len(KnowledgeRepository.list_articles(db))
        print(f"Seed completed: +{created_categories} categories, +{created_articles} articles.")
        print(f"Current totals: {total_categories} categories, {total_articles} articles.")
    finally:
        db.close()


if __name__ == "__main__":
    main()
